namespace TahoePaste.Windows.Localization;

public static class L10n
{
    private static AppLanguage _language = AppLanguageExtensions.BestMatch();

    public static event EventHandler? LanguageChanged;

    public static AppLanguage Language
    {
        get => _language;
        set
        {
            if (_language == value)
            {
                return;
            }

            _language = value;
            LanguageChanged?.Invoke(null, EventArgs.Empty);
        }
    }

    public static string Tr(string key)
    {
        var table = Tables.TryGetValue(Language, out var localized) ? localized : Tables[AppLanguage.English];
        return table.TryGetValue(key, out var value)
            ? value
            : Tables[AppLanguage.English].TryGetValue(key, out var fallback)
                ? fallback
                : key;
    }

    public static string Tr(string key, params object[] args)
    {
        return string.Format(Language.Culture(), Tr(key), args);
    }

